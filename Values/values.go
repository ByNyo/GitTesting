package main

import "fmt"

func main() {
	fmt.Println("go" + "lang") // Combines two strings and prints them

	fmt.Println("1+1 =", 1+1)         // Shows the "task" and calculates it then prints the "task" with the solution
	fmt.Println("7.0/3.0 =", 7.0/3.0) // Shows the "task" and calculates it then prints the "task" with the solution

	fmt.Println(true && false) // If something is True and Something is False. The answer is false.
	fmt.Println(true || false) // If something is True (and)or False. The answer is true.
	fmt.Println(!true)         // If something isn't true. The answer is false.
}
